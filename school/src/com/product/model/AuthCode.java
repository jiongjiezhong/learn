package com.product.model;

import com.product.model.common.BasicModel;

/**
 * 验证码
 * 
 * @author
 * 
 */
public class AuthCode extends BasicModel {

	/**
	 * serial version ID
	 */
	private static final long serialVersionUID = 7243312299592430431L;

	/** 验证码 */
	private String authCode;

	/** 手机号码 */
	private String mobileNumber;

	/**
	 * 验证码
	 * 
	 * @return 验证码
	 */
	public String getAuthCode() {
		return authCode;
	}

	/**
	 * 验证码
	 * 
	 * @param pAuthCode
	 *            验证码
	 */
	public void setAuthCode(String pAuthCode) {
		this.authCode = pAuthCode;
	}

	/**
	 * 手机号码的取得。
	 * 
	 * @return 手机号码
	 */
	public String getMobileNumber() {
		return mobileNumber;
	}

	/**
	 * 手机号码的设定。
	 * 
	 * @param pMobileNumber
	 *            手机号码
	 */
	public void setMobileNumber(String pMobileNumber) {
		this.mobileNumber = pMobileNumber;
	}

}
