package com.product.model.common;


public class BasicListAndMutiFile extends BasicMutiFileModel {

	/**
	 * serial version ID
	 */
	private static final long serialVersionUID = -3768281254272946233L;

	/** 分页用对象 */
	private DwzPagerMySQL pager;
	/**
	 * 用户web分页
	 */
    private int offset;
    private int numPerPage;
    
    
	/**
	 * 分页用对象取得
	 * 
	 * @return 分页用对象
	 */
	public DwzPagerMySQL getPager() {
		return pager;
	}

	/**
	 * 分页用对象设定
	 * 
	 * @param pPager
	 *            分页用对象
	 */
	public void setPager(DwzPagerMySQL pPager) {
		this.pager = pPager;
	}

	public int getOffset() {
		return offset;
	}

	public void setOffset(int offset) {
		this.offset = offset;
	}

	public int getNumPerPage() {
		return numPerPage;
	}

	public void setNumPerPage(int numPerPage) {
		this.numPerPage = numPerPage;
	}

}
