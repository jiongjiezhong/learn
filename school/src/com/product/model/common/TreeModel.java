package com.product.model.common;

import java.io.Serializable;
import java.util.List;

/**
 * 
 * @version V1.0
 * @author Administrator
 * @date 2014年7月3日
 */
public class TreeModel implements Serializable {

	/**
	 * serial version ID
	 */
	private static final long serialVersionUID = 7751993315385354239L;

	/** 对象KEY */
	private String modelKey;

	/** 对象名称 */
	private String modelName;

	/** 是否有子对象 */
	private boolean hasChild = false;

	/** 子对象列表 */
	List<TreeModel> childTreeModels;

	/**
	 * 对象KEY的取得。
	 * 
	 * @return 对象KEY
	 */
	public String getModelKey() {
		return modelKey;
	}

	/**
	 * 对象KEY的设定。
	 * 
	 * @param pModelKey
	 *            对象KEY
	 */
	public void setModelKey(String pModelKey) {
		this.modelKey = pModelKey;
	}

	/**
	 * 对象名称的取得。
	 * 
	 * @return 对象名称
	 */
	public String getModelName() {
		return modelName;
	}

	/**
	 * 对象名称的设定。
	 * 
	 * @param pModelName
	 *            对象名称
	 */
	public void setModelName(String pModelName) {
		this.modelName = pModelName;
	}

	/**
	 * 是否有子对象的取得。
	 * 
	 * @return 是否有子对象
	 */
	public boolean getHasChild() {
		return hasChild;
	}

	/**
	 * 是否有子对象的设定。
	 * 
	 * @param pHasChild
	 *            是否有子对象
	 */
	public void setHasChild(boolean pHasChild) {
		this.hasChild = pHasChild;
	}

	/**
	 * 子对象列表的取得。
	 * 
	 * @return 子对象列表
	 */
	public List<TreeModel> getChildTreeModels() {
		return childTreeModels;
	}

	/**
	 * 子对象列表的设定。
	 * 
	 * @param pChildTreeModels
	 *            子对象列表
	 */
	public void setChildTreeModels(List<TreeModel> pChildTreeModels) {
		this.childTreeModels = pChildTreeModels;
	}

}
