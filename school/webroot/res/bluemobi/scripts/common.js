function disabledLink(object) {
			object.attr("href", "javascript:;");
			object.removeAttr("target");
			object.removeAttr("rel");
}
function disabledButtonLink(object) {
			object.attr("class", "buttonDisabled");
			object.attr("href", "javascript:;");
			object.removeAttr("target");
			object.removeAttr("rel");
}
function disabledButton(object) {
			object.attr("class", "buttonDisabled");
			object.attr("href", "javascript:;");
			object.removeAttr("target");
			object.removeAttr("rel");
}